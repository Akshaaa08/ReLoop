// Async translation helper for dynamic content (name and description)
export const translateDynamicContent = async (name, description, category, targetLang) => {
  if (targetLang === 'en') {
    return { name, description, category };
  }
  
  try {
    const res = await fetch('/api/translate', {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({ name, description, category }),
    });
    
    if (res.ok) {
      const data = await res.json();
      return {
        name: data.translatedName,
        description: data.translatedDescription,
        category: data.translatedCategory,
      };
    }
  } catch (error) {
    console.error('Dynamic translation failed:', error);
  }
  
  return { name, description, category }; // Return original if failed
};
